// Utility function to get translated content
export function getContent(language: string, key: string): string {
  const langContent = commonContent[language as keyof typeof commonContent];
  if (langContent && langContent[key as keyof typeof langContent]) {
    return langContent[key as keyof typeof langContent];
  }
  // Fallback to English
  return commonContent.en[key as keyof typeof commonContent.en] || key;
}

